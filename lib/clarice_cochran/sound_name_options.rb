# frozen_string_literal: true

module ClariceCochran
  module SoundNameOptions
    def sound_name(sym)
      if sym == :basso
        "Basso"
      elsif sym == :blow
        "Blow"
      elsif sym == :bottle
        "Bottle"
      elsif sym == :frog
        "Frog"
      elsif sym == :funk
        "Funk"
      elsif sym == :glass
        "Glass"
      elsif sym == :hero
        "Hero"
      elsif sym == :morse
        "Morse"
      elsif sym == :ping
        "Ping"
      elsif sym == :pop
        "Pop"
      elsif sym == :purr
        "Purr"
      elsif sym == :sosumi
        "Sosumi"
      elsif sym == :submarine
        "Submarine"
      elsif sym == :tink
        "Tink"
      end
    end
  end
end
